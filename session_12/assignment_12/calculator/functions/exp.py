import math


def exp(x):
    
    """Compute exponential value of a argument x."""
    
    result = math.exp(x)
    print(f'{result}')
    return result


def dif_expo(arg):
    
    """Compute dirivative of exponential function of argument x."""
    
    result = math.exp(arg)
    print(f'{result}')
    return result
